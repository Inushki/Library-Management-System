    private void cmbBIDItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbBIDItemStateChanged
        try{
        Class.forName("java.sql.Driver");
        Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/library","root","");
        
        if(cmbBID.getSelectedIndex()>0){
        String queData="SELECT * FROM book WHERE BID='"+cmbBID.getSelectedItem()+"';";
        Statement stget=conn.createStatement();
        ResultSet data=stget.executeQuery(queData);
        
        if(data.next()){
        txtTitle.setText(data.getString("Title"));
        txtAuthor.setText(data.getString("Author"));
        cmbGenre.setSelectedItem(data.getString("Genre"));
        txtpubliYear.setText(data.getString("PubliYear"));
        txtEdition.setText(data.getString("Edition"));
        txtShelfNo.setText(data.getString("ShelfNo"));
        txtISBN.setText(data.getString("ISBN"));
        avail=data.getString("Available");
        if(avail.equals("Yes")){rbYes.setSelected(true);}
        else if(avail.equals("No")){rbNo.setSelected(true);}
        
        }
        }
        else{
        txtTitle.setText("");
        cmbGenre.setSelectedIndex(0);
        txtAuthor.setText("");
        txtpubliYear.setText("");
        txtEdition.setText("");
        txtISBN.setText("");
        txtShelfNo.setText("");
        bgavail.clearSelection();
        }
        
        }
        catch(Exception Err){
        JOptionPane.showMessageDialog(this,"Error while Changing Admin Id..."+"\n"+Err);
        }
    }//GEN-LAST:event_cmbBIDItemStateChanged
    
    private void NumGen(){
        try{
        Class.forName("java.sql.Driver");
        Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/library","root","");
        
        String queRows="SELECT COUNT(*) rowcount FROM book;";
        Statement st=conn.createStatement();
        ResultSet rs=st.executeQuery(queRows);
        rs.next();
        int NumRow=rs.getInt("rowcount");
        rs.close();
        
        if(NumRow>0){
            String getMax="SELECT MAX(serialno) FROM book;";
            Statement MaxNum=conn.createStatement();
            ResultSet maxS=MaxNum.executeQuery(getMax);
        if(maxS.next()){
            int maxSerial=Integer.parseInt(maxS.getString("MAX(serialno)"));
            maxSerial+=1;
            SNo=String.valueOf(maxSerial);
        }    
        }
        else{
            SNo="1";
        }
    BID="BID-"+SNo;
    lblBID.setText(BID);
    }
    catch(Exception myErr){
        JOptionPane.showMessageDialog(this,"Error while Number Generation..."+"\n"+myErr);
    }
    }
