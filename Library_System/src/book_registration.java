    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try{
        Class.forName("java.sql.Driver");
        Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/library","root","");
        if(rbYes.isSelected()==true){avail="Yes";}
        else if(rbNo.isSelected()==true){avail="No";}
        String queSave="INSERT INTO book(BID,Title,Author,Genre,PubliYear,Edition,ISBN,ShelfNo,Available) VALUES (?,?,?,?,?,?,?,?,?);";
        PreparedStatement cmd=conn.prepareStatement(queSave);
        cmd.setString(1,lblBID.getText());
        cmd.setString(2,txtTitle.getText());
        cmd.setString(3,txtAuthor.getText());
        cmd.setString(4,String.valueOf(cmbGenre.getSelectedItem()));
        cmd.setString(5,txtpubliYear.getText());
        cmd.setString(6,txtEdition.getText());
        cmd.setString(7,txtISBN.getText());
        cmd.setString(8,txtShelfNo.getText());
        cmd.setString(9,avail); 
        cmd.execute();
        
        JOptionPane.showMessageDialog(this,"Book: "+txtTitle.getText()+" Successfully SAVE to the database under the Book ID: "+lblBID.getText()+"!","Record Save",JOptionPane.INFORMATION_MESSAGE);
        makanna();
        }
        catch(Exception saveErr){
        JOptionPane.showMessageDialog(this,"Error while Save..."+"\n"+saveErr);
        }
    }//GEN-LAST:event_btnAddActionPerformed

